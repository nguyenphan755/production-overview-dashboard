#!/usr/bin/env node
/**
 * Render Grafana Postgres datasource from MES .env files.
 * Reads: .env → backend/.env → grafana/.env (later overrides earlier).
 *
 * Usage: node scripts/render-grafana-datasource.mjs
 */

import { readFileSync, writeFileSync, existsSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');

function loadEnvFile(path) {
  if (!existsSync(path)) return;
  for (const line of readFileSync(path, 'utf8').split('\n')) {
    const t = line.trim();
    if (!t || t.startsWith('#')) continue;
    const eq = t.indexOf('=');
    if (eq < 0) continue;
    const key = t.slice(0, eq).trim();
    let val = t.slice(eq + 1).trim();
    if (
      (val.startsWith('"') && val.endsWith('"')) ||
      (val.startsWith("'") && val.endsWith("'"))
    ) {
      val = val.slice(1, -1);
    }
    process.env[key] = val;
  }
}

// Root first, then backend, then grafana-specific overrides
loadEnvFile(join(root, '.env'));
loadEnvFile(join(root, 'backend', '.env'));
loadEnvFile(join(root, 'grafana', '.env'));

function resolvePgHost() {
  const explicit = process.env.GRAFANA_PG_HOST?.trim();
  if (explicit) return explicit;

  const dbHost = (process.env.DB_HOST || 'localhost').trim().toLowerCase();
  if (dbHost === 'localhost' || dbHost === '127.0.0.1' || dbHost === '::1') {
    return 'host.docker.internal';
  }
  return process.env.DB_HOST || 'host.docker.internal';
}

const host = resolvePgHost();
const port = process.env.GRAFANA_PG_PORT || process.env.DB_PORT || '5432';
const database = process.env.GRAFANA_PG_DATABASE || process.env.DB_NAME || 'production_dashboard';
const user = process.env.GRAFANA_PG_USER || process.env.DB_USER || 'postgres';
const password = process.env.GRAFANA_PG_PASSWORD || process.env.DB_PASSWORD || '';

if (!password) {
  console.warn(
    '⚠️  DB_PASSWORD / GRAFANA_PG_PASSWORD not set — datasource may fail to connect.'
  );
}

const yaml = `# AUTO-GENERATED by scripts/render-grafana-datasource.mjs — do not edit manually.
# Re-run: node scripts/render-grafana-datasource.mjs
# Sources: .env, backend/.env, grafana/.env

apiVersion: 1

datasources:
  - name: MES PostgreSQL
    uid: mes-postgres
    type: postgres
    access: proxy
    url: ${host}:${port}
    user: ${user}
    secureJsonData:
      password: ${JSON.stringify(password)}
    jsonData:
      database: ${database}
      sslmode: disable
      postgresVersion: 1500
      timescaledb: false
    isDefault: true
    editable: true
`;

const outPath = join(root, 'grafana', 'provisioning', 'datasources', 'postgres.yml');
writeFileSync(outPath, yaml, 'utf8');

console.log(`✅ Wrote ${outPath}`);
console.log(`   host=${host}:${port} db=${database} user=${user}`);
